    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            PreparedStatement pst = conecta.conn.prepareCall("insert into pedido (datahora,notafiscal,valorfrete,desconto,valortotal) values (?,?,?,?,?)");
             pst.setString(1,txtData.getText());
             pst.setString(2,txtNotaFiscal.getText());
             pst.setFloat(3,Float.parseFloat(txtFrete.getText()));
             pst.setFloat(4, Float.parseFloat(txtDesconto.getText()));
             pst.setFloat(5, Float.parseFloat(txtValorTotal.getText()));
             pst.executeUpdate();
             
            JOptionPane.showMessageDialog(rootPane, "Salvo com Sucesso!!!");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro na iserÃ§Ã£o!!!\n Erro: " + ex);
            
        }
                txtCodigo.setText("");
                txtDesconto.setText("");
                txtFrete.setText("");
                txtNotaFiscal.setText("");
                txtValorTotal.setText("");
                txtData.setEnabled(true);
                txtDesconto.setEnabled(true);
                txtHora.setEnabled(true);
                txtNotaFiscal.setEnabled(true);
                txtValorTotal.setEnabled(true);
                
                btnDeletar.setEnabled(true);
                btnEditar.setEnabled(true);
                btnSalvar.setEnabled(true);
                btnNovo.setEnabled(false);
                btnSair.setEnabled(true);
            
           
        
    }//GEN-LAST:event_btnSalvarActionPerformed

